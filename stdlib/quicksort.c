#include "../includes/libft.h"

int	*quicksort(int *tab, unsigned int size)
{
	unsigned int	i;
	int				temp;

	temp = 0;
	i = 0;
	while (i < (size - 1))
	{
		if (tab[i] > tab[i + 1])
		{
			temp = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = temp;
			i = 0;
		}
		else
			i++;
	}
	return (tab);
}
